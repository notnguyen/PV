# Dictionary { : }
dictionary = {"a": "a", "b": "b", "c": "c"}
dictionary2 = {9: 9, 1: 1, 7: 7, 5: 5}
dictionary3 = {True: True, False: False}

print("dictionary: " + str(dictionary))
print("dictionary2: " + str(dictionary2))
print("dictionary3: " + str(dictionary3))
print()

