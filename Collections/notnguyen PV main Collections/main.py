# id() - adresa, dir() - funkce, type() - typ


# Tuple ()
tuple = ("a", "b", "c")

print("tuple: " + str(tuple))
print(id(tuple))
print(dir(tuple))
print(type(tuple))
print()

# List []
list = ["a", "b", "c"]

print("list: " + str(list))
print(id(list))
print(dir(list))
print(type(list))
print()

# Set {}
set = {"a", "b", "c"}

print("set: " + str(set))
print(id(set))
